public class Calculadora {
    
    public static void soma(double num1, double num2){

        double soma;

        soma = num1 + num2;

        System.out.println("Resultado da Soma:\t" + soma);

    }

    public static void subtracao(double num1, double num2){

        double subtracao;

        subtracao = num1 - num2;

        System.out.println("Resultado da Subtração:\t" + subtracao);

    }

    public static void multiplicacao(double num1, double num2){

        double multiplicacao;

        multiplicacao = num1 * num2;

        System.out.println("Resultado da Multiplicacao:\t" + multiplicacao);

    }

    public static void divisao(double num1, double num2){

        double divisao;

        divisao = num1/num2;

        System.out.println("Resultado da Divisao:\t" + divisao);

    }


}
